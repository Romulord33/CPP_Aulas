#include <iostream>

using namespace std;

void somar(float *var, float valor);
void iniVetor(float *v);

int main(){
	
	float num=0;
	float vetor[5];
	
	somar(&num,15);     //Agora é operado no endereço da variavel num, não irá usar outro endereço de memória. Pois sem o ponteiro o programa não enxerga a variável
	iniVetor(vetor);     //Quande é vetor não precisa especificar o endereço
	
	cout << "\n" << num << "\n\n";
	
	for(int i=0; i<5; i++){
		cout << vetor[i] << "\n";
	}
	
	return 0;
}

void somar(float *var, float valor){
	*var+=valor;
}

void iniVetor(float *v){
	v[0]=0;     //Não precisa indicar que é um vetor, no caso, colocar *v[0]=0
	v[1]=0;
	v[2]=0;
	v[3]=0;
	v[4]=0;
}
